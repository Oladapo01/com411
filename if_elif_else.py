print("Towards what direction are you painting?")
direction=input()
if direction == "up":
    print("I am painting in an upwards direction!")
elif direction == "down":
    print("I am painting in a downward direction!")
elif direction == "right":
    print("I am painting in a towards right!")
elif direction == "left":
    print("I am painting towards the left!")
else:
    print("Wrong direction!")